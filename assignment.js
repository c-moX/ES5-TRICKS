//kilometerToMeter 01
function kilometerToMeter(kiloMeter) {
    var meter = 1;
    if (kiloMeter < 0) {
        return "Sorry, You are lost";
    } else {
        var meter = 1000 * kiloMeter;
        return meter;
    }
}
