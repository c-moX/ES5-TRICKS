function budgetCalculator(watch, phone, laptop) {
    if (watch, phone, laptop != 'number' || watch, phone, laptop < 0) {
        return "Please enter your valid number";
    } else {
        return watch * 50 + phone * 100 + laptop * 500;
    }
}
